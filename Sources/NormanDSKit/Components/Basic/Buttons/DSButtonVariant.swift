/// Visual and behavioral variants for `DSButton`.
///
/// Use a variant to choose the button's emphasis and styling within the design system.
///
/// - primary: High-emphasis button used for primary actions.
/// - secondary: Medium-emphasis button with outlined or subtle background styling.
/// - tertiary: Low-emphasis, text-like button.
public enum DSButtonVariant {
    case primary
    case secondary
    case tertiary
}

